
public class ForLoopApp {

	public static void main(String[] args) {
		System.out.println("Hey, from this app!");
		
		//p. 139
		//loop that prints #s 1 - 20 (adding "+=" to it adds to the counter - play with this)
		for (int i = 1; i <= 20; i+=2) {
			System.out.println("The number is: "+i);
		}
		
		
		System.out.println("Bye, from this app!");

	}

}
